package com.lisanbian.pojo;

public class SportInfor {
    private String name;//用户名

    private String sportTime;//运动时间

    private String sportTimeLonge;//运动持续时间

    private String energy;//消耗能力

    private String sportWay;//运动方式

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSportTime() {
        return sportTime;
    }

    public void setSportTime(String sportTime) {
        this.sportTime = sportTime;
    }

    public String getSportTimeLonge() {
        return sportTimeLonge;
    }

    public void setSportTimeLonge(String sportTimeLonge) {
        this.sportTimeLonge = sportTimeLonge;
    }

    public String getEnergy() {
        return energy;
    }

    public void setEnergy(String energy) {
        this.energy = energy;
    }

    public String getSportWay() {
        return sportWay;
    }

    public void setSportWay(String sportWay) {
        this.sportWay = sportWay;
    }
}
